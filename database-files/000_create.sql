
DROP DATABASE IF EXISTS CityPlanner;

CREATE DATABASE IF NOT EXISTS CityPlanner;

USE CityPlanner;

CREATE TABLE Region (
	region_id INT PRIMARY KEY NOT NULL,
	regionName VARCHAR(200) NOT NULL,
	population BIGINT
);

CREATE TABLE Location (
    location_id INT PRIMARY KEY NOT NULL,
    latitude DECIMAL(10, 8) NOT NULL,
    longitude DECIMAL(11, 8) NOT NULL,
    region_id INT,
    FOREIGN KEY (region_id) REFERENCES Region(region_id)
);
CREATE TABLE Urban_Green_Spaces (
    zone_id INT PRIMARY KEY,
    area_size DECIMAL NOT NULL,
    proposed_conversion_type VARCHAR(100),
    location_id INT NOT NULL,

    CONSTRAINT fk_green_space_location
        FOREIGN KEY (location_id)
        REFERENCES Location(location_id)
);

CREATE TABLE Building (
    building_id INT PRIMARY KEY,
    energy_consumption DECIMAL NOT NULL,
    location_id INT NOT NULL,
    CONSTRAINT fk_building_location
        FOREIGN KEY (location_id)
        REFERENCES Location(location_id)
);

CREATE TABLE Energy_Consumption_Alert (
    alert_id INT PRIMARY KEY,
    alert_type VARCHAR(50) NOT NULL,
    building_id INT NOT NULL,
    exceeded_amt DECIMAL(10,2) NOT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_alert_building
        FOREIGN KEY (building_id)
        REFERENCES Building(building_id)
);

CREATE TABLE CO2_Emissions (
    emission_id INT PRIMARY KEY,
    source VARCHAR(100) NOT NULL,
    emission_level DECIMAL NOT NULL,
    timestamp TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    location_id INT NOT NULL,
    building_id INT,

    CONSTRAINT fk_emission_location
        FOREIGN KEY (location_id)
        REFERENCES Location(location_id),
    CONSTRAINT fk_emission_building
        FOREIGN KEY (building_id)
        REFERENCES Building(building_id)
);

CREATE TABLE EV_Infrastructure (
    station_id INT PRIMARY KEY,
    usage_level DECIMAL(5, 2),
    energy_consumption DECIMAL(10, 2) NOT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    location_id INT NOT NULL,

    CONSTRAINT fk_ev_location
        FOREIGN KEY (location_id)
        REFERENCES Location(location_id)
);

CREATE TABLE Air_Quality_Metrics (
    aq_id INT PRIMARY KEY,
    AQI INT NOT NULL,
    pollutant_type VARCHAR(50) NOT NULL,
    location_id INT NOT NULL,
    station_id INT,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_aq_location
        FOREIGN KEY (location_id)
        REFERENCES Location(Location_id),

    CONSTRAINT fk_aq_ev_station
        FOREIGN KEY (station_id)
        REFERENCES EV_Infrastructure(station_id)
  );

CREATE TABLE Simulation_Model (
    model_id INT PRIMARY KEY,
    predicted_outcomes VARCHAR(100) NOT NULL,
    type VARCHAR(50) NOT NULL,
    zone_id INT,
    emission_id INT,
    aq_id INT,

CONSTRAINT fk_model_green_space
        FOREIGN KEY (zone_id)
        REFERENCES Urban_Green_Spaces(zone_id),

CONSTRAINT fk_model_emission
        FOREIGN KEY (emission_id)
        REFERENCES CO2_Emissions(emission_id),

CONSTRAINT fk_model_air_quality
        FOREIGN KEY (aq_id)
        REFERENCES Air_Quality_Metrics(aq_id)
);

CREATE TABLE Truck (
    truck_id INT PRIMARY KEY,
    capacity DECIMAL(8,2) NOT NULL,
    route_id INT
);


CREATE TABLE Recycling_Data (
    recycling_id INT PRIMARY KEY,
    weight DECIMAL(10,2) NOT NULL,
    collection_date DATE NOT NULL,
    location_id INT NOT NULL,
    truck_id INT NOT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    rate DECIMAL(5,2),

CONSTRAINT fk_recycling_location
        FOREIGN KEY (location_id)
        REFERENCES Location(Location_id),

CONSTRAINT fk_recycling_truck
    FOREIGN KEY (truck_id)
    REFERENCES Truck(truck_id)
);

CREATE TABLE Sensor_Data (
    sensor_id INT PRIMARY KEY,
    timestamp TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    parameter VARCHAR(50) NOT NULL,
    location_id INT NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'active',
    building_id INT,

CONSTRAINT fk_sensor_location
        FOREIGN KEY (location_id)
        REFERENCES Location(location_id),

CONSTRAINT fk_sensor_building
        FOREIGN KEY (building_id)
        REFERENCES Building(building_id)
);

CREATE TABLE Department (
    department_id INT PRIMARY KEY NOT NULL,
    name VARCHAR(200) NOT NULL,
    budget DECIMAL(15, 2)
);

CREATE TABLE Project (
    project_id INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
    name VARCHAR(200) NOT NULL,
    status ENUM('proposed', 'inProgress', 'completed') NOT NULL,
    budget DECIMAL(15, 2),
    description TEXT,
    sustainabilityScore INT,
    timeToCompletion INT,
    region_id INT,
    department_id INT,
    FOREIGN KEY (region_id) REFERENCES Region(region_id),
    FOREIGN KEY (department_id) REFERENCES Department(department_id)
);

CREATE TABLE Users (
    user_id INT PRIMARY KEY NOT NULL,
    username VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    role VARCHAR(30) NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE,
    phone_number VARCHAR(20),
    last_login TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Citizen_Report (
    report_id INT PRIMARY KEY NOT NULL,
    status VARCHAR(50) NOT NULL,
    reportDate DATE NOT NULL,
    description TEXT,
    location_id INT NOT NULL,
    user_id INT NOT NULL,
    CONSTRAINT fk_report_location FOREIGN KEY (location_id) REFERENCES Location(location_id),
    CONSTRAINT fk_report_user FOREIGN KEY (user_id) REFERENCES Users(user_id)
);

CREATE TABLE Monitoring_Config (
    config_id INT PRIMARY KEY,
    job VARCHAR(255),
    importance ENUM('low', 'medium', 'high') NOT NULL,
    monitor_condition TEXT,
    monitor_interval INT
);

CREATE TABLE Incidents (
    incident_id INT PRIMARY KEY,
    end_time DATETIME,
    start_time DATETIME,
    severity ENUM('low', 'medium', 'high') NOT NULL,
    status VARCHAR(50)
);

CREATE TABLE Config_Incident (
    config_id INT,
    incident_id INT,
    service_uptime FLOAT,
    PRIMARY KEY (config_id, incident_id),
    FOREIGN KEY (config_id) REFERENCES Monitoring_Config(config_id),
    FOREIGN KEY (incident_id) REFERENCES Incidents(incident_id)
);

CREATE TABLE User_Tickets (
    ticket_id INT PRIMARY KEY,
    status ENUM('open', 'in-progress', 'closed') NOT NULL,
    title VARCHAR(255) NOT NULL,
    description TEXT,
    assignee_id INT,
    filer_id INT NOT NULL,
    FOREIGN KEY (assignee_id) REFERENCES Users(user_id),
    FOREIGN KEY (filer_id) REFERENCES Users(user_id)
);

CREATE TABLE Crew (
    crew_id INT PRIMARY KEY,
    crew_name VARCHAR(100) NOT NULL,
    specialty VARCHAR(100) NOT NULL,
    location_id INT NOT NULL,
    status VARCHAR(50) NOT NULL,
    size INT,

    CONSTRAINT fk_crew_location
        FOREIGN KEY (location_id)
        REFERENCES Location(location_id)
);

CREATE TABLE Issue_Log (
    issue_id INT PRIMARY KEY,
    issue_type VARCHAR(100) NOT NULL,
    crew_id INT,
    priority VARCHAR(20) NOT NULL,
    status VARCHAR(50) NOT NULL,
    location_id INT NOT NULL,
    description TEXT,
    traffic_impact VARCHAR(50),
    report_id INT,

    CONSTRAINT fk_issue_crew
        FOREIGN KEY (crew_id)
        REFERENCES Crew(crew_id),

    CONSTRAINT fk_issue_location
        FOREIGN KEY (location_id)
        REFERENCES Location(location_id),

    CONSTRAINT fk_issue_citizen_report
        FOREIGN KEY (report_id)
        REFERENCES Citizen_Report(report_id)
);

CREATE TABLE Maintenance_Log (
    log_id INT PRIMARY KEY,
    completed_date DATE,
    description TEXT,
    issue_id INT,

    CONSTRAINT fk_issue_log
        FOREIGN KEY (issue_id)
        REFERENCES Issue_Log(issue_id)
);

CREATE TABLE Infrastructure_Type (
    type_id INT PRIMARY KEY,
    issue_id INT,
    type VARCHAR(100) NOT NULL,
    location_id INT,
    priority VARCHAR(50),

    CONSTRAINT fk_infra_issue
        FOREIGN KEY (issue_id) REFERENCES Issue_Log(issue_id),

    CONSTRAINT fk_infra_location
        FOREIGN KEY (location_id) REFERENCES Location(location_id)
);

